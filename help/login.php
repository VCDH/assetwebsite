<h1>Aanmelden</h1>
<p>Je gebruikersnaam op deze website is het e-mailadres wat je bij het aanmaken van een account hebt opgegeven. Het wachtwoord is bij registratie per e-mail toegezonden en kan na aanmelden desgewenst gewijzigd worden.</p>

<h1>Account aanmaken</h1>
<p>Klik op de link <a href="?do=signup">Registreren</a> om een account voor deze website te maken. Om assets te bekijken is geen account nodig. Om cameralocaties te bekijken moet je beschikken over een e-mailadres van een organisatie die hiervoor gemachtigd is.</p>

<h1>Wachtwoord vergeten</h1>
<p>Als je je wachtwoord vergeten bent, klik dan op de link <a href="?do=lostpass">Wachtwoord vergeten</a>. Na opgeven van je e-mailadres kun je jezelf een nieuw wachtwoord laten toezenden. Na aanmelden met dit nieuwe wachtwoord kun je dit desgewenst zelf wijzigen in iets anders.</p>

<h1>Registreren</h1>
<p>Met een account voor deze website worden je naam, e-mailadres en telefoonnummer vooraf ingevuld in DRIP-aanvraagformulieren die je aanmaakt. Als je bovendien registreert met het e-mailadres van een gemachtigde organisatie kun je niet-publieke informatie bekijken zoals cameralocaties, procedures inzien en worden de e-mailadressen waar een DRIP-aanvraagformulier ingediend moet worden afgebeeld op het formulier.</p>
