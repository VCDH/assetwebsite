<h1>DRIP-aanvraagformulier</h1>

<p class="warning">Het DRIP-aanvraagformulier is in gebruik bij wegbeheerders in Zuid-Holland die verenigd zijn in het samenwerkingsplatform <a href="https://www.bereiknu.nl/" target="_blank">BEREIK!</a>. Voor andere regio's kunnen andere procedures van toepassing zijn.</p>

<p>Het DRIP-aanvraagformulier wordt gebruikt om inzet van DRIPs aan te vragen bij Rijkswaterstaat Verkeerscentrale Zuid-West Nederland, provincie Zuid-Holland, gemeente Rotterdam en gemeente Den Haag. Het gaat hierbij dan om &quot;eenvoudige&quot; aanvragen voor inzet van DRIPs bij wegwerkzaamheden en evenementen waarbij geen aanvullende (file)schakelingen nodig zijn. Het DRIP-aanvraagformulier is niet geschikt voor het aanvragen voor het opstellen van een regelscenario, hiervoor gelden andere procedures.</p>

<p>De wegbeheerder op wiens grondgebied de wegwerkzaamheden of het evenement plaatsvinden is in beginsel verantwoordelijk voor het tijdig doen van een DRIP-aanvraag bij de relevante collega-wegbeheerders. Hierbij is enige kennis van het opstellen van regelscenario's onontbeerlijk om een goede aanvraag te kunnen doen, aangezien een DRIP-aanvraag eigenlijk een mini-regelscenario is. Het doen van een aanvraag geeft geen garantie dat de aangevraagde DRIPs daadwerkelijk beschikbaar zijn. De aanvrager moet hier in zijn doorlooptijd rekening mee houden om indien nodig tijdig andere maatregelen te kunnen treffen als een of meerdere DRIPs onverhoopt niet beschikbaar zijn.</p>

<h2>Werkwijze DRIP-aanvraag</h2>
<ol>
    <li>Selecteer de gewenste DRIPs door ze aan te klikken op de kaart of in de tabel.</li>
    <li>Klik op de knop <i>Aanvraagformulier downloaden</i>. Er wordt nu gevraagd om het aanvraagformulier te downloaden of op te slaan op de computer.</li>
    <li>Open het aanvraagformulier (een Word-document) en vul dit verder in. De geselecteerde DRIPs zijn al opgenomen in het aanvraagformulier. DRIP-teksten kunnen worden ontworpen met bijvoorbeeld de <a href="http://wdd.s200.nl" target="_blank">WebDRIP Designer</a>.</li>
    <li>Stuur het aanvraagformulier naar de relevante wegbeheerders. Deze zijn vermeld onderaan het aanvraagformulier.</li>

</ol>

