<h1>Account</h1>
<p>Via deze pagina kun je je weergavenaam en wachtwoord wijzigen.</p>

<h1>Gebruikersgegevens wijzigen</h1>
<p>Hier kun je je naam en telefoonnummer opgegeven. Wanneer opgegeven worden deze automatisch vooraf ingevuld in een DRIP-aanvraagformulier.</p>

<h1>Wachtwoord wijzigen</h1>
<p>Bij registreren wordt een willekeurig wachtwoord generereerd. Dat kun je hier wijzigen in iets wat je wel kunt onthouden. Het nieuwe wachtwoord moet uit minimaal acht karakters bestaan, verder zijn er geen bijzondere voorwaarden die aan het wachtwoord gesteld worden. Kies zelf een veilig wachtwoord dat niet door anderen geraden kan worden. Een veilig wachtwoord is opgebouwd uit willekeurige kleine letters, hoofdletters, cijfers en speciale tekens op het toetsenbord.</p>